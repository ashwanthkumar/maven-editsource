package in.ashwanthkumar.maven.editsource;

public class Input {
    private String input;
    private String output;

    public String getInput() {
        return input;
    }

    public String getOutput() {
        return output;
    }

    @Override
    public String toString() {
        return "Input{" +
                "input='" + input + '\'' +
                ", output='" + output + '\'' +
                '}';
    }
}
